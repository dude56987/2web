<div class='titleCard linkInfo'>
	<h2 id="using_links">🔗 Direct Links</h2>
	<p>
		On all pages containing the web player there will be a direct link button.
	</p>
	<span class='button'>🔗 Direct Link</span>
	<p>
		This links directly to the source content on the server. Clicking this link on most any platform will open a appropriate player. This is the simplest way to view any content that the webplayer can't handle in your browser.
	</p>
	<p>
		If the link is to a external resource this button will link to the exit page.
	</p>
	<p>
	On android you can hold the button and "use open with external app" or "share" from the popup
	menu to play the link with your video player. Below are some good open source video players for android.
	<ul>
		<li>
			<a target="_new" href='https://play.google.com/store/apps/details?id=is.xyz.mpv'>MPV</a>
		</li>
		<li>
			<a target="_new" href='https://play.google.com/store/apps/details?id=org.videolan.vlc'>VLC</a>
		</li>
	</ul>
	</p>
	<p>
	Some content can still not be played in the browsers by default, so if anything refuses to play this generally gets around it.
	</p>
</div>
